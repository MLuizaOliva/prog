""" Faça um Programa que peça 
as 4 notas bimestrais e mostre a média """

numa = float(input("Digite o primeiro numero:"))
numb = float(input("Digite o segundo numero:"))
numc = float(input("Digite o terceiro numero:"))
numd = float(input("Digite o quarto numero:"))

media = (numa + numb + numc + numd) / 4

print(media)